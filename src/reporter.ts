  /**
   * Get JavaScript for interactivity
   */
  private getScript(): string {
    return `
    function toggleSection(header) {
      const section = header.closest('.type-section');
      section.classList.toggle('collapsed');
    }

    // Expand failed sections by default
    document.querySelectorAll('.result-card.fail').forEach(card => {
      const section = card.closest('.type-section');
      section.classList.remove('collapsed');
    });

    // Collapse passing sections by default
    document.querySelectorAll('.type-section').forEach(section => {
      const hasFailed = section.querySelector('.result-card.fail');
      if (!hasFailed) {
        section.classList.add('collapsed');
      }
    });
    `;
  }

  /**
   * Escape HTML special characters
   */
  private escapeHtml(text: string): string {
    const map: Record<string, string> = {
      '&': '&amp;',
      '<': '&lt;',
      '>': '&gt;',
      '"': '&quot;',
      "'": '&#039;'
    };
    return text.replace(/[&<>"']/g, char => map[char]);
  }

  /**
   * Open file in browser
   */
  private async openInBrowser(filePath: string): Promise<void> {
    const { platform } = process;
    const { exec } = await import('child_process');

    const command = platform === 'darwin'
      ? `open "${filePath}"`
      : platform === 'win32'
        ? `start "" "${filePath}"`
        : `xdg-open "${filePath}"`;

    exec(command);
  }

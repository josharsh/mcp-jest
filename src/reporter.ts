/**
 * Generate HTML report (convenience function)
 */
export async function generateHTMLReport(
  suite: TestSuite,
  options?: ReporterOptions,
  serverInfo?: { command: string; args?: string[] }
): Promise<string> {
  const reporter = new HTMLReporter(options);
  return reporter.generate(suite, serverInfo);
}
